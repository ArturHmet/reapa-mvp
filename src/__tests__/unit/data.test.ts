import { describe, it, expect } from "vitest";
import {
  leads,
  clients,
  tasks,
  dashboardStats,
  funnelData,
  leadSourceData,
} from "@/lib/data";

describe("lib/data — leads", () => {
  it("exports a non-empty leads array", () => {
    expect(Array.isArray(leads)).toBe(true);
    expect(leads.length).toBeGreaterThanOrEqual(5);
  });

  it("every lead has required string fields", () => {
    for (const lead of leads) {
      expect(typeof lead.id).toBe("string");
      expect(typeof lead.name).toBe("string");
      expect(typeof lead.phone).toBe("string");
      expect(typeof lead.email).toBe("string");
      expect(typeof lead.location).toBe("string");
      expect(typeof lead.propertyType).toBe("string");
      expect(typeof lead.nationality).toBe("string");
      expect(typeof lead.message).toBe("string");
      expect(typeof lead.notes).toBe("string");
      expect(typeof lead.createdAt).toBe("string");
      expect(typeof lead.lastContact).toBe("string");
    }
  });

  it("every lead has valid score", () => {
    const valid = ["hot", "warm", "cold"];
    for (const lead of leads) {
      expect(valid).toContain(lead.score);
    }
  });

  it("every lead has valid source", () => {
    const valid = ["whatsapp", "email", "portal", "instagram", "facebook", "referral"];
    for (const lead of leads) {
      expect(valid).toContain(lead.source);
    }
  });

  it("every lead has numeric budget and bedrooms", () => {
    for (const lead of leads) {
      expect(typeof lead.budget).toBe("number");
      expect(lead.budget).toBeGreaterThan(0);
      expect(typeof lead.bedrooms).toBe("number");
      expect(lead.bedrooms).toBeGreaterThan(0);
    }
  });

  it("every lead has boolean autoReplied", () => {
    for (const lead of leads) {
      expect(typeof lead.autoReplied).toBe("boolean");
    }
  });

  it("includes at least one hot and one warm lead", () => {
    expect(leads.some((l) => l.score === "hot")).toBe(true);
    expect(leads.some((l) => l.score === "warm")).toBe(true);
  });

  it("lead ids are unique", () => {
    const ids = leads.map((l) => l.id);
    expect(new Set(ids).size).toBe(ids.length);
  });
});

