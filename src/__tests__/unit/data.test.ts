describe("lib/data — dashboardStats", () => {
  it("has all required numeric fields", () => {
    expect(typeof dashboardStats.totalLeads).toBe("number");
    expect(typeof dashboardStats.hotLeads).toBe("number");
    expect(typeof dashboardStats.activeClients).toBe("number");
    expect(typeof dashboardStats.viewingsToday).toBe("number");
    expect(typeof dashboardStats.pendingTasks).toBe("number");
    expect(typeof dashboardStats.overdueTasks).toBe("number");
    expect(typeof dashboardStats.dealsThisMonth).toBe("number");
    expect(typeof dashboardStats.revenue).toBe("number");
    expect(typeof dashboardStats.conversionRate).toBe("number");
  });

  it("has avgResponseTime as string", () => {
    expect(typeof dashboardStats.avgResponseTime).toBe("string");
    expect(dashboardStats.avgResponseTime.length).toBeGreaterThan(0);
  });

  it("conversionRate is in range 0–100", () => {
    expect(dashboardStats.conversionRate).toBeGreaterThanOrEqual(0);
    expect(dashboardStats.conversionRate).toBeLessThanOrEqual(100);
  });

  it("hotLeads <= totalLeads", () => {
    expect(dashboardStats.hotLeads).toBeLessThanOrEqual(dashboardStats.totalLeads);
  });

  it("overdueTasks <= pendingTasks", () => {
    expect(dashboardStats.overdueTasks).toBeLessThanOrEqual(dashboardStats.pendingTasks);
  });
});

describe("lib/data — funnelData", () => {
  it("exports a non-empty funnel array", () => {
    expect(Array.isArray(funnelData)).toBe(true);
    expect(funnelData.length).toBeGreaterThanOrEqual(3);
  });

  it("each stage has stage/count/color", () => {
    for (const stage of funnelData) {
      expect(typeof stage.stage).toBe("string");
      expect(typeof stage.count).toBe("number");
      expect(typeof stage.color).toBe("string");
      expect(stage.color).toMatch(/^#[0-9a-fA-F]{6}$/);
    }
  });

  it("funnel is top-heavy (first stage has most leads)", () => {
    expect(funnelData[0].count).toBeGreaterThan(funnelData[funnelData.length - 1].count);
  });
});

describe("lib/data — leadSourceData", () => {
  it("exports a non-empty sources array", () => {
    expect(Array.isArray(leadSourceData)).toBe(true);
    expect(leadSourceData.length).toBeGreaterThanOrEqual(4);
  });

  it("each source has source/count/percentage", () => {
    for (const src of leadSourceData) {
      expect(typeof src.source).toBe("string");
      expect(typeof src.count).toBe("number");
      expect(typeof src.percentage).toBe("number");
    }
  });

  it("percentages sum to ~100", () => {
    const total = leadSourceData.reduce((sum, s) => sum + s.percentage, 0);
    expect(total).toBeGreaterThanOrEqual(90);
    expect(total).toBeLessThanOrEqual(110);
  });
});
